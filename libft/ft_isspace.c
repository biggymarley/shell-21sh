#include "libft.h"

char	*ft_isspace(const char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
				|| str[i] == '\f' || str[i] == '\r'
				|| str[i] == ' ')
			i++;
		else
			break ;
	}
	return ((char *)str + i);
}
