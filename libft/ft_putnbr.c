#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int i;

	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	i = n;
	if (i < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(i / 10);
		ft_putchar(i % 10 + '0');
	}
}
